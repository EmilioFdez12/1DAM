package prog.unidad04.actividad204.ejercicio06;

public class Punto {
	
	private double x;
	private double y;
	
	public Punto(double x, double y) {
		this.x = x;
		this.y = y;
	}
	/**
	 * 
	 * @return La coordenada X del punto
	 */
	public double getX() {
		return x;
	}
	
	/**
	 * 
	 * @return La coordenada Y del punto
	 */
	public double getY() {
		return y;
	}
	
	
}
